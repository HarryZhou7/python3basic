# 字典dict key value
# {key1:value1,key2:value2...}
type({1:1,2:2,3:3})

{'Q':"新月打击",'W':"苍白之瀑",'E':"月之降临",'R':"月神冲刺"}  # 集合类型

# 通过key得到value
{'Q':"新月打击",'W':"苍白之瀑",'E':"月之降临",'R':"月神冲刺"} ['Q']
{'Q':"新月打击",'W':"苍白之瀑",'E':"月之降临",'R':"月神冲刺"} ['R']

# 字典不能有相同的key(键)
{1:"新月打击",'1':"苍白之瀑",'E':"月之降临",'R':"月神冲刺"}   # key可以是字符串也可以是数字

# value:str int float list set dict
# key必须是不可变类型，int、str、tuple

type({})  # 空的字典